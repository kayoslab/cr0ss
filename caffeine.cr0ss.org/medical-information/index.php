<h2>Physical impact</h2>
<p>
  The physical data our application gathers from your Smartphone can never substitute the opinion of a trained specialist. See it as a helper to determine the personal limitation for your caffeine consumption, which should not be trespassed. For our calculations we have recourse to a great list of public and open access scientific documents on the caffeine half-life and it’s repercussions on the human body. With this we want to assure that the given advices are as accurately as possible. For users with a significant higher sensibility (e.g. pregnant woman, cardiacs, children) these calculations are inapplicable. Please contact a doctor regarding your recommended daily consumption limits.
</p>
